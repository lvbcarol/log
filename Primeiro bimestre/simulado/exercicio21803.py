#Crie um programa em Python que sugira uma atividade de lazer baseada na estação do ano preferida do usuário. As estações do ano e as atividades sugeridas são definidas da seguinte maneira:

#* Verão: Ir à praia;
#* Outono: Fazer uma trilha;
#* Inverno: Assistir uma série;
#* Primavera: Passear no parque.

#O programa deve pedir ao usuário que selecione sua estação do ano preferida e, com base nessa escolha, exibir a atividade de lazer recomendada.

estação = input("Qual a sua estação do ano preferida?")
if estação == ("verão"):
    print ("ir à praia")
elif estação == ("outono"):
    print ("fazer uma trilha")
elif estação == ("inverno"):
    print ("assistir uma série")
elif estação == ("primavera"):
    print ("passear no parque")
 
    
