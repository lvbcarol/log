#Elaborar um programa Python que permita a leitura de dois números inteiros e exiba o maior deles. Use estrutura Se-Então-Senão.
numeroA = int(input("Digite o primeiro número:"))
numeroB = int(input("Digite o segundo número:"))
if numeroA > numeroB:
    print (numeroA)
else:
    print (numeroB)